/// Gets the public IPv4 address.
///
/// - [source] The URL to use to obtain the public IP address. Defaults to
/// 'https://api.ipify.org'.
/// 
/// Returns the public IP address as a [String] or `null` if the address could
/// not be obtained.
Future<String?> getPublicIPV4Address({
  String source = 'https://api.ipify.org',
}) async {
  try {
    final response = await get(Uri.parse(source));
    if (response.statusCode == 200) {
      return response.body.nullIfEmpty;
    }
  } catch (_) {}
  return null;
}
