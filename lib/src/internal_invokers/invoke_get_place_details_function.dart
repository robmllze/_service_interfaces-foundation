Future<THttpFunctionResult> invokeGetPlaceDetailsFunction({
  required FunctionsServiceInterface functionsBroker,
  required AuthServiceInterface authServiceBroker,
  required String placeId,
}) async {
  final idToken = await authServiceBroker.getIdToken();
  final url = functionsBroker.functionsEndpointUrl('getPlaceDetails');
  final response = await post(
    url,
    headers: {
      'Content-Type': 'application/json',
      'Authorization': 'Bearer $idToken',
    },
    body: jsonEncode({
      'placeId': placeId,
    }),
  );

  if (response.statusCode == 200) {
    return (response: response, success: true);
  } else {
    return (response: response, success: false);
  }
}
