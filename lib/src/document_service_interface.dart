abstract class DocumentServiceInterface<T extends Model> extends ModelServiceInterface<T> {
  //
  //
  //

  final String id;

  //
  //
  //

  DocumentServiceInterface({
    required super.serviceEnvironment,
    required this.id,
  });

  //
  //
  //

  @override
  Stream<T> stream() {
    return this
        .serviceEnvironment
        .databaseServiceBroker
        .streamModel(this.databaseRef())
        .map((e) => this.fromJson(e?.data ?? {}));
  }
}
