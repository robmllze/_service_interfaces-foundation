class UserInterface {
  //
  //
  //

  final String userId;
  final String email;
  final String? displayName;
  final String? photoURL;
  final bool? emailVerified;

  //
  //
  //

  const UserInterface({
    required this.userId,
    required this.email,
    this.displayName,
    this.photoURL,
    this.emailVerified,
  });

  //
  //
  //

  Uri? get photoURI {
    return this.photoURL == null ? null : Uri.tryParse(this.photoURL!);
  }

  //
  //
  //

  String get userPubId {
    return IdUtils.mapUserIdToPubId(userId: userId);
  }
}